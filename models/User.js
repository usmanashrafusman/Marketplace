import mongoose from "mongoose";
const { Schema , model } = mongoose;

// createing a schema to add user data in db
const UserSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  timestamp: {
    type: Date,
    default: Date.now,
  },
});

const User = model("user", UserSchema);
export default User;
